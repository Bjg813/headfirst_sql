-- Inner Join:
    -- Any join that combines the records from two tables using some condition

-- Natural Join:
    -- An inner join that leaves off the 'ON' clause. It only works if you are joining two tables that have the same column name

-- Equijoin:
    -- Inner join returns rows that are equal

-- Non-Equijoin:
    -- Returns any rows that are not equal

-- Cross Join:
    -- Returns every row from one table crossed with every row from the second table
    -- Known by many other names including CARTESIAN JOIN and NO JOIN

-- Comma Join:
    -- The same thing as a CROSS JOIN, except a comma is used instead of the keywords CROSS JOIN